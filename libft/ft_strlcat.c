#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t	i;
	size_t	dst_len;
	size_t	src_len;

	dst_len = ft_strlen(dst);
	src_len = ft_strlen(src);
	i = 0;
	if (dst_len >= size)
		return (size + src_len);
	while (dst_len + i < size && *(src + i))
	{
		*(dst + dst_len + i) = *(src + i);
		i++;
	}
	if (size == dst_len + i)
		i--;
	*(dst + dst_len + i) = '\0';
	return (dst_len + src_len);
}
